def _binding(provider: str, expose_name: str) -> dict:
    if provider == "gcp":
        return {
            "platform": "gcp",
            "format": "bigquery_table",
            "location": {
                "project": "${FLUID_GCP_PROJECT}",
                "dataset": "analytics",
                "table": expose_name,
            },
        }
    if provider == "snowflake":
        return {
            "platform": "snowflake",
            "format": "table",
            "location": {
                "database": "${SNOWFLAKE_DATABASE}",
                "schema": "ANALYTICS",
                "table": expose_name.upper(),
            },
        }
    return {
        "platform": "local",
        "format": "csv",
        "location": {"path": f"runtime/out/{expose_name}.csv"},
    }


def _minimal_contract(
    *,
    provider: str = "local",
    template: str = "analytics",
    domain: str = "analytics",
    owner: str = "data-team",
    columns: dict[str, str] | None = None,
) -> dict:
    expose_name = "orders_output"
    schema_columns = [
        {
            "name": name,
            "type": {
                "integer": "integer",
                "number": "number",
                "date": "date",
                "datetime": "timestamp",
            }.get(column_type, "string"),
            "required": False,
        }
        for name, column_type in (columns or {"id": "integer", "email": "string"}).items()
    ]
    return {
        "fluidVersion": "0.7.2",
        "kind": "DataProduct",
        "id": "generated.test",
        "name": "Test Product",
        "description": "Generated by fake copilot scenario tests",
        "domain": domain,
        "metadata": {
            "layer": "Bronze",
            "owner": {"team": owner, "email": "data-team@example.com"},
        },
        "builds": [
            {
                "id": "main_build",
                "pattern": "embedded-logic",
                "engine": "sql",
                "properties": {"sql": "SELECT 1 AS id"},
                "execution": {
                    "trigger": {"type": "manual", "iterations": 1},
                    "runtime": {"platform": provider, "resources": {"cpu": "1", "memory": "2Gi"}},
                },
            }
        ],
        "exposes": [
            {
                "exposeId": expose_name,
                "kind": "table",
                "binding": _binding(provider, expose_name),
                "contract": {"schema": schema_columns},
                "semantics": {
                    "name": "Orders Output",
                    "description": "Semantic model for scenario tests.",
                    "entities": [{"name": schema_columns[0]["name"], "type": "primary"}],
                    "measures": [
                        {
                            "name": f"{schema_columns[0]['name']}_count",
                            "agg": "count",
                            "expr": schema_columns[0]["name"],
                            "description": "Scenario count metric.",
                        }
                    ],
                    "dimensions": [{"name": schema_columns[0]["name"], "type": "categorical"}],
                    "metrics": [
                        {
                            "name": f"{schema_columns[0]['name']}_count_metric",
                            "type": "simple",
                            "measure": f"{schema_columns[0]['name']}_count",
                            "description": "Scenario metric.",
                        }
                    ],
                },
            }
        ],
    }


