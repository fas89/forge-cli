def test_copilot_scenario_validation_repair_loop_uses_second_attempt(tmp_path: Path, monkeypatch):
    workspace = tmp_path / "repair-loop"
    _write_sample_workspace(workspace, "csv")
    monkeypatch.chdir(workspace)

    agent = _make_agent()
    captured_prompts = []
    bad_payload = _payload(provider="local", template="starter")
    good_payload = _payload(provider="local", template="analytics")

    def _capture_llm(_provider, _config, _system_prompt, user_prompt):
        captured_prompts.append(json.loads(user_prompt))
        if len(captured_prompts) == 1:
            return json.dumps(bad_payload)
        return json.dumps(good_payload)

    with patch("fluid_build.cli.forge.resolve_llm_config", return_value=_mock_llm_config()):
        with patch(
            "fluid_build.cli.forge.build_capability_matrix", return_value=_capability_matrix()
        ):
            with patch(
                "fluid_build.cli.forge_copilot_runtime.validate_generated_result",
                side_effect=[(["First attempt failed validation"], []), ([], [])],
            ):
                with patch(
                    "fluid_build.cli.forge_copilot_runtime.call_llm", side_effect=_capture_llm
                ):
                    result = agent.generate_project_artifacts(
                        {
                            "project_goal": "Orders product",
                            "data_sources": "csv files",
                            "use_case": "analytics",
                        },
                        {"llm_provider": "openai", "target_dir": str(workspace)},
                    )

    assert len(result.attempt_reports) == 2
    assert result.attempt_reports[0].validation_errors == ["First attempt failed validation"]
    assert captured_prompts[1]["repair_feedback"] == ["First attempt failed validation"]
    assert captured_prompts[1]["previous_response_summary"]["recommended_template"] == "starter"
    assert result.suggestions["recommended_template"] == "analytics"


def test_copilot_scenario_corrupt_memory_is_ignored_then_show_and_reset_work(
    tmp_path: Path, monkeypatch
):
    workspace = tmp_path / "memory-management"
    _write_common_workspace(workspace)
    store = CopilotMemoryStore(workspace)
    store.path.parent.mkdir(parents=True, exist_ok=True)
    store.path.write_text("{not-json", encoding="utf-8")
    monkeypatch.chdir(workspace)

    agent = _make_agent()
    agent._create_with_forge_engine = lambda project_config, dry_run=False: True
    agent._show_next_steps = lambda *args, **kwargs: None
    agent._show_ai_analysis = lambda *args, **kwargs: None

    with patch("fluid_build.cli.forge.resolve_llm_config", return_value=_mock_llm_config()):
        with patch(
            "fluid_build.cli.forge.build_capability_matrix", return_value=_capability_matrix()
        ):
            with patch(
                "fluid_build.cli.forge_copilot_runtime.validate_generated_result",
                return_value=([], []),
            ):
                with patch(
                    "fluid_build.cli.forge_copilot_runtime.call_llm",
                    return_value=json.dumps(_payload(provider="local", template="starter")),
                ):
                    success = agent.create_project(
                        workspace,
                        {
                            "project_goal": "Fresh starter",
                            "data_sources": "local tables",
                            "use_case": "analytics",
                        },
                        {
                            "non_interactive": True,
                            "save_memory": True,
                            "llm_provider": "openai",
                            "target_dir": str(workspace),
                        },
                    )

    assert success is True
    memory = store.load()
    assert memory is not None
    assert memory.project_profile["template"] == "starter"

    show_result = handle_memory_management(
        SimpleNamespace(target_dir=str(workspace), show_memory=True, reset_memory=False),
        logger=None,
    )
    assert show_result == 0
    assert store.path.exists()

    reset_result = handle_memory_management(
        SimpleNamespace(target_dir=str(workspace), show_memory=False, reset_memory=True),
        logger=None,
    )
    assert reset_result == 0
    assert not store.path.exists()
