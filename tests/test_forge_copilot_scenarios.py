def _write_common_workspace(path: Path) -> None:
    path.mkdir(parents=True, exist_ok=True)
    (path / "README.md").write_text(
        "# Fake project\n\nUsed for copilot scenarios.\n", encoding="utf-8"
    )
    (path / "sql").mkdir(exist_ok=True)
    (path / "sql" / "orders.sql").write_text(
        "select * from raw.orders join raw.customers using (customer_id)\n",
        encoding="utf-8",
    )


def _write_sample_workspace(
    path: Path, sample_format: str
) -> tuple[dict[str, str], dict[str, object]]:
    _write_common_workspace(path)
    samples_dir = path / "samples"
    samples_dir.mkdir(exist_ok=True)

    if sample_format == "csv":
        (samples_dir / "customers.csv").write_text(
            "id,email,active\n1,alice@example.com,true\n2,bob@example.com,false\n",
            encoding="utf-8",
        )
        return {"id": "integer", "email": "string", "active": "boolean"}, {}

    if sample_format == "json":
        (samples_dir / "customers.json").write_text(
            json.dumps(
                [
                    {"id": 1, "email": "alice@example.com", "active": True},
                    {"id": 2, "email": "bob@example.com", "active": False},
                ]
            ),
            encoding="utf-8",
        )
        return {"id": "integer", "email": "string", "active": "boolean"}, {}

    if sample_format == "parquet":
        (samples_dir / "customers.parquet").write_bytes(b"PAR1")
        return (
            {"customer_id": "integer", "created_at": "datetime", "country": "string"},
            {
                "patch_target": "fluid_build.cli.forge_copilot_schema_inference.read_parquet_metadata",
                "patch_value": {
                    "columns": {
                        "customer_id": "integer",
                        "created_at": "datetime",
                        "country": "string",
                    },
                    "row_count": 12,
                    "schema_source": "pyarrow",
                },
            },
        )

    if sample_format == "avro":
        (samples_dir / "customers.avro").write_bytes(b"Obj\x01")
        return (
            {"customer_id": "integer", "created_at": "datetime", "country": "string"},
            {
                "patch_target": "fluid_build.cli.forge_copilot_schema_inference.read_avro_metadata",
                "patch_value": {
                    "columns": {
                        "customer_id": "integer",
                        "created_at": "datetime",
                        "country": "string",
                    },
                    "schema_source": "fastavro",
                },
            },
        )

    raise ValueError(f"Unsupported sample format: {sample_format}")


def _write_gcp_workspace(path: Path) -> None:
    _write_common_workspace(path)
    (path / "dbt_project.yml").write_text(
        "name: fake_project\nprofile: bigquery\nmodel-paths: ['models']\n",
        encoding="utf-8",
    )
    (path / "infra").mkdir(parents=True, exist_ok=True)
    (path / "infra" / "main.tf").write_text(
        'resource "google_bigquery_dataset" "analytics" {}\n',
        encoding="utf-8",
    )


@pytest.mark.parametrize("sample_format", ["csv", "json", "parquet", "avro"])
def test_copilot_scenario_mock_samples_save_metadata_only_memory(
    tmp_path: Path, monkeypatch, sample_format: str
):
    workspace = tmp_path / f"workspace-{sample_format}"
    expected_columns, patch_config = _write_sample_workspace(workspace, sample_format)
    monkeypatch.chdir(workspace)

    agent = _make_agent()
    agent._create_with_forge_engine = lambda project_config, dry_run=False: True
    agent._show_next_steps = lambda *args, **kwargs: None
    agent._show_ai_analysis = lambda *args, **kwargs: None

    with ExitStack() as stack:
        stack.enter_context(
            patch("fluid_build.cli.forge.resolve_llm_config", return_value=_mock_llm_config())
        )
        stack.enter_context(
            patch(
                "fluid_build.cli.forge.build_capability_matrix", return_value=_capability_matrix()
            )
        )
        stack.enter_context(
            patch(
                "fluid_build.cli.forge_copilot_runtime.validate_generated_result",
                return_value=([], []),
            )
        )
        stack.enter_context(
            patch(
                "fluid_build.cli.forge_copilot_runtime.call_llm",
                return_value=json.dumps(
                    _payload(
                        provider="local",
                        template="analytics",
                        columns=expected_columns,
                    )
                ),
            )
        )
        if patch_config:
            stack.enter_context(
                patch(patch_config["patch_target"], return_value=patch_config["patch_value"])
            )
        success = agent.create_project(
            workspace,
            {
                "project_goal": f"{sample_format} analytics project",
                "data_sources": f"{sample_format} files",
                "use_case": "analytics",
            },
            {
                "non_interactive": True,
                "save_memory": True,
                "llm_provider": "openai",
                "target_dir": str(workspace),
            },
        )

    assert success is True

    store = CopilotMemoryStore(workspace)
    memory = store.load()
    assert memory is not None
    assert memory.project_profile["template"] == "analytics"
    assert memory.project_profile["provider"] == "local"
    assert memory.conventions["build_engines"] == ["sql"]
    assert sample_format in memory.conventions["source_formats"]
    assert memory.conventions["schema_summaries"][0]["columns"] == expected_columns

    raw_memory = store.path.read_text(encoding="utf-8")
    assert "alice@example.com" not in raw_memory
    assert "bob@example.com" not in raw_memory
    assert "SELECT * FROM raw.orders" not in raw_memory


